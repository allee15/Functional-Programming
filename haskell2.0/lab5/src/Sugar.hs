sugarExp :: Exp -> ComplexExp
sugarExp (X x)= CX (sugarVar x)
sugarExp (App e1 e2)= CApp (App (sugarExp e1) (sugarExp e2))
sugarExp (Lam x e) = CLam (sugarVar x)

